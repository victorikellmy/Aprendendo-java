package Ex3;

public class Medicamento {
    int ano_atual;
    int ano_fanricacao;
    int tempo_vencimento;

    public int getAno_atual() {
        return ano_atual;
    }

    public void setAno_atual(int ano_atual) {
        this.ano_atual = ano_atual;
    }

    public int getAno_fanricacao() {
        return ano_fanricacao;
    }

    public void setAno_fanricacao(int ano_fanricacao) {
        this.ano_fanricacao = ano_fanricacao;
    }

    public int getTempo_vencimento() {
        return tempo_vencimento;
    }

    public void setTempo_vencimento(int tempo_vencimento) {
        this.tempo_vencimento = tempo_vencimento;
    }

}
